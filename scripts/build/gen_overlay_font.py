def row_to_byte(s):
    b = 0
    for i in range(8):
        if i < len(s) and s[i] == '#':
            b |= (0x80 >> i)
    return b

FIRST, LAST = 0x20, 0x5F  # space .. underscore
out = []
out.append("// Generated by scripts/build/gen_overlay_font.py -- DO NOT EDIT BY HAND.")
out.append("// 8x8 font, one byte per row (MSB = leftmost pixel). ASCII 0x20..0x5F.")
out.append("#ifndef FONT8X8_H")
out.append("#define FONT8X8_H")
out.append("#include <stdint.h>")
out.append(f"#define FONT8X8_FIRST 0x{FIRST:02X}")
out.append(f"#define FONT8X8_LAST  0x{LAST:02X}")
out.append("static const uint8_t font8x8[%d][8] = {" % (LAST - FIRST + 1))
for cp in range(FIRST, LAST + 1):
    ch = chr(cp)
    glyph = G.get(ch, G[' '])
    bytes_ = ",".join("0x%02X" % row_to_byte(r) for r in glyph)
    label = ch if ch != '\\' else '\\\\'
    out.append(f"    {{{bytes_}}},  // 0x{cp:02X} '{label}'")
out.append("};")
out.append("#endif // FONT8X8_H")

dst = os.path.join(os.path.dirname(__file__), "..", "..", "include", "font8x8.h")
with open(os.path.normpath(dst), "w") as f:
    f.write("\n".join(out) + "\n")
